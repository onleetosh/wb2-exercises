package com.pluralsight;

public class DoWhileLoop {
    public static void main(String[] args) {

        //create a do-while loop to display the text "I love Java" 5 times.
        int z = 0;
        do {
            System.out.println("\"I love Java !\"");
            z++;
        }
        while ( z < 5);
    }
}
